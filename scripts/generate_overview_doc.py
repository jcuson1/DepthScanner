def set_default_font(doc, name="Arial", size_pt=12):
    style = doc.styles["Normal"]
    style.font.name = name
    style.font.size = Pt(size_pt)


def add_title(doc, text):
    p = doc.add_paragraph()
    p.alignment = WD_PARAGRAPH_ALIGNMENT.CENTER
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(20)


def add_h2(doc, text):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(15)


def add_h3(doc, text):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(13)


def add_p(doc, text):
    doc.add_paragraph(text)


def add_bullet(doc, text):
    doc.add_paragraph(text, style="List Bullet")


